package main

import "fmt"

func main() {
	str := "mandelbrot.go"
	str = basename(str)
	fmt.Printf("basename: %s\n", str)
}

func basename(s string) string {
	for i := len(s) - 1; i >= 0; i-- {
		if s[i] == '/' {
			s = s[i+1:]
			break
		}
	}

	for i := len(s) - 1; i >= 0; i-- {
		if s[i] == '.' {
			s = s[:i]
			break
		}
	}

	return s
}
